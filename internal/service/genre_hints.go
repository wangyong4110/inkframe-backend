// genreSceneHints 返回注入 chapter_scene_outline.j2 的类型特化场景设计规则。
// 规范化场景结构、冲突机制和类型核心节拍，确保场景大纲符合该类型的叙事惯例。
func genreSceneHints(genre string) string {
	g := strings.ToLower(strings.TrimSpace(genre))
	switch {
	case contains(g, "修仙", "仙侠", "xianxia"):
		return `【修仙/仙侠——场景设计规则】
- 境界差距决定冲突逻辑：战斗场景须体现境界压制感，低境界角色必须有具体的创意应对（非无理突破），对手的压制感要从感知层面外化（灵压、气场、领域）
- 突破/渡劫场景：内魔（心理障碍）与外部干扰必须并行——只有外部危机或只有内心挣扎，都不够完整；必须有"不可逆的代价"
- 机缘/宝物场景：设计争夺序列——感知→吸引多方→竞争升级→险境→得失并存；机缘与代价绑定，不可无代价获取
- 门派/宗门场景：规则（门规/天道）与情义之间的取舍是核心冲突源；角色的选择必须让两个价值都受损
- 天地感应：重要转折处用天地异象（灵气骤变、异兽朝圣、天穹异色）作为叙事信号，每章至多一次，滥用则失效`

	case contains(g, "玄幻", "奇幻", "fantasy", "magic"):
		return `【玄幻/奇幻——场景设计规则】
- 势力格局场景：主角的每次行动须触动至少两方势力的利益，单线推进的情节显得格局太小
- 法宝/异宝场景：争夺序列——发现→多方知晓→竞争→危机→得失决定后续格局
- 天才对决场景：展示各自独特的道途理念与战斗哲学，而非单纯的力量数值比拼；观战者的反应是体现差距的最佳手段
- 机缘场景：机缘必须绑定代价（时间、关系、暴露秘密、埋下隐患），主角光环是质量杀手
- 世界观展示场景：通过不同势力/文化/修炼体系的碰撞来展示世界深度，而非旁白式的背景介绍`

	case contains(g, "武侠", "wuxia"):
		return `【武侠——场景设计规则】
- 江湖规矩场景：门派恩怨、行规、仇怨链必须在行动中起实质约束作用；"江湖规矩"既是推进器也是枷锁
- 比武/切磋场景：武学境界差异体现在"见招拆招的质量"和"武学见解的高低"，而非蛮力；高手往往以一招定胜负
- 义气/恩义场景：通过具体行动（出手相救、替人受过、主动担责）而非台词表达，口头说义气反而削弱张力
- 江湖局势场景：每个新势力的登场必须打乱原有格局，而非只是增加一个新面孔
- 隐逸/出世场景：欲退江湖的角色必然被"身不由己的最后一件事"拉回，设计这个"最后一件事"须足够分量`

	case contains(g, "历史", "古代", "宫廷", "historical"):
		return `【历史/古代——场景设计规则】
- 朝堂/宫廷场景：礼制规范和权力格局决定站位、开口顺序和言辞分寸；谁先开口、谁沉默，本身就是权力信号
- 阶级场景：用称谓和礼仪行为体现等级差异，避免现代化的平等视角侵入；细节处的僭越或失礼即为冲突导火索
- 谋略场景：设计"一步棋动、三处响应"的连锁反应，让读者能感受到棋局感；局中人的信息不对称是谋略的核心
- 情感场景：情感必须借助器物、诗词、隐语外化，不可直白表达；"见月怀人"强于"她很想念他"
- 危机场景：明线（事件）与暗线（关系/权力格局）并进，一个事件必须同时触动至少两条关系线`

	case contains(g, "都市", "现代", "contemporary", "urban", "modern"):
		return `【都市/现代——场景设计规则】
- 对话场景必须有"权力翻转"时刻：主角从被动到主动，或从主动到被动；静止的对话只是信息交换，不是戏剧
- 误会场景：设计"主角合理无法开口解释"的具体原因，不能只靠巧合；误会的维持要有内在逻辑
- 接近/关系发展场景：利用第三方事件（共同危机/尴尬处境/外部压力）缩短心理距离，直接推进反而失真
- 争吵/矛盾场景：真正的冲突源是价值观差异或深层恐惧，表面事件只是导火索；写清楚"是什么让他们无法理解对方"
- 醋意/嫉妒场景：外化为行动而非心理描述；通过第三方事件或不经意的过度反应触发，不直接写"他吃醋了"`

	case contains(g, "言情", "romance", "爱情"):
		return `【言情——场景设计规则】
- 情感推进场景：每场景必须让人物关系发生至少一处细微但不可逆的变化（亲疏程度/信任层级/权力平衡）
- 心动场景设计：通过"意外的细节捕捉"触发心动（对方做了某件小事，主角发现自己记住了），而非外貌描写
- 告白/表白前必须有"意识到自己在乎对方"的确认场景，否则告白显得突兀
- 误会/分离场景：设计"最坏的时机"——真相揭露的时机总在关系最脆弱的时刻
- 糖分场景（甜宠）：日常小细节的互动质量高于大事件的轰炸——"帮她系扣子"比"送豪宅"更甜`

	case contains(g, "科幻", "sci-fi", "science fiction", "未来", "赛博", "cyberpunk"):
		return `【科幻/赛博朋克——场景设计规则】
- 技术细节场景：科技的使用必须有物理后果和感官反应（不只是"启动了设备"，而是设备启动后环境/感知的具体变化）
- 人机交互场景：设计信息不对称（人类不理解机器的某个反应，或机器以意外方式理解了人类意图），这是科幻独特的戏剧张力
- 社会矛盾场景：技术进步带来的阶级/伦理撕裂作为冲突背景；个人事件要折射更大的社会矛盾
- 动作/追逃场景：充分利用环境科技元素（全息遮蔽/重力异常/监控盲区/信号干扰）制造独特的场景逻辑
- 情感场景：用科技隐喻情感（数据丢失=遗忘，系统重启=重新开始），冷硬背景反衬出情感的稀缺与珍贵`

	case contains(g, "末世", "末日", "apocalyptic", "灾难"):
		return `【末世/灾难——场景设计规则】
- 资源争夺场景：设计"数量/质量/时间"三重压力同时出现；没有理想解，只有代价最小的选择
- 信任危机场景：危险情境才能真正测试角色立场；"关键时刻的选择"比日常行为更能揭示人物
- 生存决策场景：每个选择必须有实质代价（放弃一个人/暴露位置/消耗不可再生资源），零代价决策没有张力
- 威胁场景：先写环境异变（动物异常/气味/温度变化/声音停止）再写威胁本体，让读者先于角色感知到危险
- 团队内部矛盾场景：外部压力下的内部分裂——"怎么活下去"的价值观差异外化为具体冲突（不是争吵，是行动上的分歧）`

	case contains(g, "悬疑", "推理", "犯罪", "惊悚", "mystery", "thriller"):
		return `【悬疑/推理——场景设计规则】
- 每个场景同时推进两层叙事：表层行动线（发生了什么）+ 信息线（读者获得了哪条线索，失去了哪条线索）
- 嫌疑人场景：每个出场人物须有看似合理的动机和不在场证明；信息须对读者公平——读者有机会推理
- 推理场景：主角每次观察必须给出一个推断（哪怕是错误推断）；只有观察没有推断的场景叙事无力
- 揭示场景：真相必须通过推理过程展开，禁止全知视角直接说出答案；揭示前需有一次"假胜利"让读者以为谜题已解
- 氛围场景：用具体感官细节（不寻常的气味/异样的声音/温度差异）制造认知失调，禁止直接写"感到恐惧"`

	case contains(g, "游戏", "电竞", "网游", "game"):
		return `【游戏/网游——场景设计规则】
- 战斗/副本场景：设计职业协作逻辑（各角色的技能功能互补），战斗胜负由策略决定而非数值碾压
- Boss战场景：阶段式设计——探索弱点→策略执行→意外变数→最终突破；每阶段须有一次主角判断失误的代价
- 游戏内社交场景：利用匿名性/角色扮演/信息不对称制造独特的人际张力（现实身份vs游戏身份）
- 竞技/排名场景：对手须有明确的战术风格和心理特点，胜负前必须先让对手展示其核心战术
- 现实-游戏切换场景：两个世界的情感必须互相渗透，游戏中的事件对现实产生影响（反之亦然）`

	case contains(g, "童话", "寓言", "fairy", "fable"):
		return `【童话/寓言——场景设计规则】
- 考验场景：三次考验的递进结构（第一次给提示、第二次真正测试、第三次颠覆预期），每次考验考核不同的品质
- 道德抉择场景：善恶的界限须清晰，但选择的代价须真实；"做好事有代价"比"好事一帆风顺"更有深度
- 奇幻元素场景：魔法/魔法物品须有规则约束（有代价、有限制、有意外），无限制的魔法消解张力
- 变形/成长场景：外在形态的变化须对应内在价值观的变化；变形既是惩罚也是启示
- 结局场景：寓意须落在行动和结果上，而非道德说教；让故事本身说话，不需要额外的总结性教训`

	default:
		return ""
	}
}

// genreWritingHints 返回注入 chapter_from_outline.j2 的类型特化写作风格规则。
// 规范化散文风格、对话写法、心理描写密度和节奏，确保正文符合该类型的阅读期待。
func genreWritingHints(genre string) string {
	g := strings.ToLower(strings.TrimSpace(genre))
	switch {
	case contains(g, "修仙", "仙侠", "xianxia"):
		return `【修仙/仙侠——正文写作规则】
- 境界感知写法：用具体感官描述修为变化，而非数值（"灵识已能探知三里之外山石纹理"）；周围环境的反应是修为的最佳注脚（鸟雀不敢近身/灵气自动回避）
- 功法招式：写物理过程 + 天地元素响应，禁止空洞特效描述（"掌心金光一闪然后对方飞出去"不是功法，是特效）
- 修炼时间感：入定一日可感觉只是片刻——时间感的错位是仙侠意境的核心节拍之一，每章至少有一处
- 高手对决：以"意境/道意"层面的交锋为主，肉体层面的伤害为辅；高手的强大通过观战者的反应和环境变化体现
- 禁用词：升级、刷怪、加点、血量、蓝量、经验值等游戏化表达——这些词会立刻破坏仙侠的世界观沉浸感`

	case contains(g, "玄幻", "奇幻", "fantasy", "magic"):
		return `【玄幻/奇幻——正文写作规则】
- 体系感：功法/道途/宝物的描写要有内在逻辑，读者能感受到体系的自洽；体系破口比实力压制更能制造紧张感
- 势力格局：每次重要行动后用一两句交代对各方格局的影响，让读者感受到棋局感
- 史诗感：在角色的视野局限内保持宏大的世界感——通过他人的震惊反应、传说碎片、禁地的存在暗示世界的深度
- 对决写法：展示各自道途理念的碰撞，让读者能理解"他们为什么会用这种方式战斗"
- 禁用：无缘由的主角光环；系统/面板/签到等游戏化元素（除非是专门的系统流）；"天才就是天才"式的空洞强调`

	case contains(g, "武侠", "wuxia"):
		return `【武侠——正文写作规则】
- 武打描写：通过气息、步伐、眼神等前兆构建武打氛围，动作本身简洁有力——高手过招往往一两招定输赢
- 江湖语言：对话带有江湖行话和规矩意识（"在下""足下""承让""江湖规矩"），但不堆砌，自然融入即可
- 义气表达：通过行动而非台词（出手挡刀、默默跟随、替人受过）；口头说"我义薄云天"是最弱的义气表达
- 出手选择：每次"路见不平拔刀相助"须有内心的犹豫/权衡，侠义有代价才有分量；莽撞无代价的侠义是幼稚
- 江湖作为背景：江湖的规则和文化始终在背景中运作，主角的行动置于这个背景下才有意义`

	case contains(g, "历史", "古代", "宫廷", "historical"):
		return `【历史/古代——正文写作规则】
- 对话写法：借称谓、礼仪用语、典故隐语传递信息，减少直白陈述；"语带双关"是宫廷对话的基本形态
- 心理外化：用目光、手势、停顿和体态代替大篇幅内心独白；宫廷人物不轻易展示内心
- 权谋描写：结果的描写要有棋局感（"这一步落定，三处皆动"）；让读者感受到每个决定背后的多层算计
- 情感表达：借景/物/典故外化情感；"他盯着窗外的月色，再没有说话"强于"他很想念她"
- 节奏控制：正式场合（朝堂/宴会/议事）慢节奏、礼仪细节密集；私下场合放松节奏，真实人性才能流露`

	case contains(g, "都市", "现代", "contemporary", "urban", "modern"):
		return `【都市/现代——正文写作规则】
- 对话写法：≤50字的台词不加心理解释，靠语气和语境传递情绪；解释太多会稀释台词的杀伤力
- 人物刻画：强势/魅力通过他人的反应和细节行为体现，禁止自我描述（"他知道自己很优秀"是最差的刻画）
- 心理写法：用身体感受替代情绪命名（"胃部一紧"代替"她感到紧张"；"手心发热"代替"她心跳加速"）
- 节奏控制：紧张/对抗场景用短句+段落留白；暧昧/甜蜜/慢热场景放缓至长句，让读者在句子里停留
- 保持情感留白：必须保留至少一层"未说出口的情感"——不要写尽角色的心理活动，给读者自己感受的空间`

	case contains(g, "言情", "romance", "爱情"):
		return `【言情——正文写作规则】
- 情感三层写法：每场景的情感必须有"表层行为 + 内心感受 + 未说出口的真实"三层；只写一层是流水账
- 暗流写法：用"多余的动作"传递情感（他替她整了整领口，说的却是工作；她走后他盯着她坐过的椅子三秒）
- 心理节奏：思念/心动时内心独白可细腻绵长；争吵/告别时反而压缩，留白——情绪最激烈时最无话可说
- 感官细节：气味和温度是言情最有效的情绪触发器（"他身上的松木香""她的手很凉"）
- 禁用：大量形容词堆砌；"她美极了/他帅极了"等空洞赞美——用具体细节代替（"她笑起来左边有一个浅浅的酒窝"）`

	case contains(g, "科幻", "sci-fi", "science fiction", "未来", "赛博", "cyberpunk"):
		return `【科幻/赛博朋克——正文写作规则】
- 技术描写：科技效果必须有感官落点（"全息投影展开时，空气中散着淡淡的臭氧气味"）；技术细节通过感知体验，而非参数说明
- 陌生化效果：让读者通过角色的感知体验世界的"不同寻常"；日常化的未来感比炫技式的参数堆砌更有沉浸力
- 术语使用：自创术语须在首次出现时附上感官或功能层面的解释（"量子锚定——那种感觉像是被人捏住了脊梁"）
- 情感写法：在冷硬的技术背景下，情感的细微流露有更大张力；用反差制造情绪（机器面前的人类温度）
- 节奏控制：动作/追逃场景短句密集；世界观展示/人物内省允许舒缓的观察性描写，让陌生世界慢慢渗入`

	case contains(g, "末世", "末日", "apocalyptic", "灾难"):
		return `【末世/灾难——正文写作规则】
- 环境描写：强调常规事物的"失常状态"（超市货架是空的/路灯半明半灭/城市静得只剩风声）；失常本身就是恐惧
- 资源意识：角色在每个动作里保持资源计算的底层逻辑（还剩几发子弹/这一趟能否安全返回），但不需要每次都写出来
- 暴力/死亡描写：只写后果和体态变化，不写过程细节；心理冲击来自"之后的沉默"而非"当时的血腥"
- 希望与绝望交替：纯粹的绝望让读者放弃；在最黑暗处植入一个细小的希望信号（一颗种子/一个孩子的笑/一句话）
- 角色内核：末世中人物的每个行动都服从其"生存价值观"（有人保护弱者、有人选择独活），价值观的碰撞是核心戏剧`

	case contains(g, "悬疑", "推理", "犯罪", "惊悚", "mystery", "thriller"):
		return `【悬疑/推理——正文写作规则】
- 叙事视角严格限制：主角（叙述者）不能获取自己尚未掌握的信息；视角越严格，悬疑感越强
- 线索植入：3个可疑细节中只有1个是真线索，其余2个是合理干扰；读者须有公平推理的机会
- 恐惧写法：用"认知失调"制造不安（他以为门关上了，回头却发现门开着）；直接写"感到恐惧"等于放弃制造恐惧
- 揭示节奏：真相前必须有一次"假胜利"——读者以为谜题已解，然后被打脸；真正的揭示才更有冲击力
- 禁止：全知叙述者提前暗示答案；角色突然拥有无从获取的信息；"巧合"连续出现超过一次（第二个巧合即作弊）`

	case contains(g, "游戏", "电竞", "网游", "game"):
		return `【游戏/网游——正文写作规则】
- 战斗描写：聚焦策略判断和临场反应，而非技能特效；"他意识到对方在等他先动"比"他释放了大招"更有张力
- 游戏体验感官化：将游戏内的感知具象化（"手指还没到键盘，意识已经算出了三步之后的局面"）
- 双重身份写法：游戏内的冷静/强势与现实中的真实性格形成对比；两面都要写，不能只写一面
- 团队配合：每个队员有自己的战术风格和心理特点，配合的化学反应是网游故事的核心看点
- 禁止：过度沉迷于技能说明书式描写；游戏内感情的戏剧化程度超过现实（游戏感情要有节制）`

	case contains(g, "童话", "寓言", "fairy", "fable"):
		return `【童话/寓言——正文写作规则】
- 语言风格：简洁明快，句子短，动词有力；避免复杂的从句结构，让故事的节奏像音乐一样自然流动
- 奇幻描写：魔法/神奇事物须有规则约束（有代价/有限制/有意外）；无限制的魔法消解张力
- 道德重量：好人做好事须有代价和考验，善意不是无代价的；这才是让寓意深刻的关键
- 情感写法：角色的情感简单而真实（高兴/害怕/难过/坚定），不需要复杂的内心层次，但表达要具体可感
- 结尾写法：寓意须落在结果和行动上，而非道德总结；让故事本身说话，作者不需要开口教训读者`

	default:
		return ""
	}
}

func contains(s string, keywords ...string) bool {
	for _, k := range keywords {
		if strings.Contains(s, k) {
			return true
		}
	}
	return false
}
